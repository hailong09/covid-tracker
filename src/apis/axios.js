export const convertFormat  = (data) => {
    return {
        ...data,
        updated: dateFormat(new Date(parseInt(data.updated)), "dddd, mmmm dS, yyyy, h:MM:ss TT"),
        deaths:  new Intl.NumberFormat({style: 'currency'}).format(data.deaths),
        cases: new Intl.NumberFormat({style: 'currency'}).format(data.cases),
        recovered: new Intl.NumberFormat({style: 'currency'}).format(data.recovered),
        todayCases: new Intl.NumberFormat({style:'currency'}).format(data.todayCases),
        todayDeaths: new Intl.NumberFormat({style:'currency'}).format(data.todayDeaths),
        todayRecovered: new Intl.NumberFormat({style:'currency'}).format(data.todayRecovered)
    }
}
